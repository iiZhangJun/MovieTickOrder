package com.icss.entity;

public class TMovieHall {
	private String hno;
	private String cid;
	private String hname;
	private String allRowsNum;
	private String allColNum;
	
	public String getHno() {
		return hno;
	}
	public void setHno(String hno) {
		this.hno = hno;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public String getAllRowsNum() {
		return allRowsNum;
	}
	public void setAllRowsNum(String allRowsNum) {
		this.allRowsNum = allRowsNum;
	}
	public String getAllColNum() {
		return allColNum;
	}
	public void setAllColNum(String allColNum) {
		this.allColNum = allColNum;
	}
}
